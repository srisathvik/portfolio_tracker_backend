package com.sathvik.portfolio_tracker.dto;

import java.math.BigDecimal;
import java.time.LocalDate;

public class StockResponseDto {

    private String ticker;
    private String stockName;
    private double quantity;
    private BigDecimal investedValue;
    private BigDecimal currentValue;

    private LocalDate purchaseDate;

    private String errorMessage;

    public StockResponseDto() {
    }

    public StockResponseDto(String ticker, String stockName, double quantity, BigDecimal investedValue, BigDecimal currentValue, LocalDate purchaseDate, String errorMessage) {
        this.ticker = ticker;
        this.stockName = stockName;
        this.quantity = quantity;
        this.investedValue = investedValue;
        this.currentValue = currentValue;
        this.purchaseDate = purchaseDate;
        this.errorMessage = errorMessage;
    }

    public String getTicker() {
        return ticker;
    }

    public void setTicker(String ticker) {
        this.ticker = ticker;
    }

    public String getStockName() {
        return stockName;
    }

    public void setStockName(String stockName) {
        this.stockName = stockName;
    }

    public double getQuantity() {
        return quantity;
    }

    public void setQuantity(double quantity) {
        this.quantity = quantity;
    }

    public BigDecimal getInvestedValue() {
        return investedValue;
    }

    public void setInvestedValue(BigDecimal investedValue) {
        this.investedValue = investedValue;
    }

    public BigDecimal getCurrentValue() {
        return currentValue;
    }

    public void setCurrentValue(BigDecimal currentValue) {
        this.currentValue = currentValue;
    }

    public LocalDate getPurchaseDate() {
        return purchaseDate;
    }

    public void setPurchaseDate(LocalDate purchaseDate) {
        this.purchaseDate = purchaseDate;
    }

    public String getErrorMessage() {
        return errorMessage;
    }

    public void setErrorMessage(String errorMessage) {
        this.errorMessage = errorMessage;
    }
}
